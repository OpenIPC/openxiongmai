void sensor_init_k03(void) {
  XM_U16 u16Num, u16i;
  u16Num = sizeof(gau16SnsInit_k03) / sizeof(gau16SnsInit_k03[0]);
  sensor_write_register((XM_U32)gau16SnsInit_k03[0][0],
                        (XM_U32)gau16SnsInit_k03[0][1]); // reset all registers
  SysDelay_ms(50);
  for (u16i = 1; u16i < u16Num; u16i++) {
    sensor_write_register((XM_U32)gau16SnsInit_k03[u16i][0],
                          (XM_U32)gau16SnsInit_k03[u16i][1]);
  }
  DEBUG("------------- K03 5M 25fps  init ok! "
        "(@20180312_158x4)----------------\n");
}
